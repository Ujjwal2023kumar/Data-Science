    ROLLNO NUMBER PRIMARY KEY,
    NAME VARCHAR(50),
    DEPTID NUMBER
);

CREATE TABLE DEPARTMENT (
    DEPTID NUMBER PRIMARY KEY,
    DEPTNAME VARCHAR(50)
