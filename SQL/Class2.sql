-- INNER JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
INNER JOIN DEPARTMENT D
ON S.DEPTID = D.DEPTID;

-- OUTER JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
LEFT JOIN DEPARTMENT D ON S.DEPTID = D.DEPTID
UNION
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
RIGHT JOIN DEPARTMENT D ON S.DEPTID = D.DEPTID;

-- LEFT JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
LEFT JOIN DEPARTMENT D
ON S.DEPTID = D.DEPTID;

-- RIGHT JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
RIGHT JOIN DEPARTMENT D
ON S.DEPTID = D.DEPTID;

-- CROSS JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
CROSS JOIN DEPARTMENT D;

-- THETA JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S
JOIN DEPARTMENT D
ON S.DEPTID > D.DEPTID;

-- EQUI JOIN
SELECT S.ROLLNO, S.NAME, D.DEPTNAME
FROM STUDENTS S, DEPARTMENT D
WHERE S.DEPTID = D.DEPTID;

-- NATURAL JOIN
SELECT *
FROM STUDENTS
NATURAL JOIN DEPARTMENT;
